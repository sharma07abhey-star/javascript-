// numbers from 1 to 10 using while loop 
let index = 0
while (index < 11){
    console.log(`the value of index is ${index}`);
    index++
}
console.log("\n")

// while loop on array
let ar = 0
let arr = [ "abhey","parneet","sharma","virk"]
while(ar < arr.length){
    console.log(`val is ${arr[ar]}`);
    ar++
}