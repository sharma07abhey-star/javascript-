// numbers
const score = 69;
const balance = new Number(1000);
// This line creates a variable named balance with the value of 1000, but it explicitly 
// forces JavaScript to treat it as a complex "Object" rather than a simple, lightweight number.

console.log(score);
console.log(balance);

console.log(balance.toString());


// math   // inbuild functions of math in javascript

console.log(Math);
console.log(Math.abs(-4));  // abs converts negative to positive
console.log(Math.round(4.5));  // round rounds to nearest integer
console.log(Math.ceil(4.2));  // ceil rounds up to nearest integer
console.log(Math.floor(4.9));  // floor rounds down to nearest integer
console.log(Math.max(4,8,9,76,34));  // max returns largest value
console.log(Math.min(4,8,9,76,34));  // min returns smallest value

console.log(Math.random()); // the value always comes in 0 to 1 in random .
